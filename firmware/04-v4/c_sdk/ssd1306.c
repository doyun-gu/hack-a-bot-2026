/* ---- Low-level I2C helpers ---- */

static void ssd1306_cmd(ssd1306_t *dev, uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};  /* Co=0, D/C#=0 = command */
    i2c_write_blocking(dev->i2c, dev->addr, buf, 2, false);
}

/* ---- Public API ---- */

bool ssd1306_init(ssd1306_t *dev, i2c_inst_t *i2c, uint8_t addr) {
    dev->i2c = i2c;
    dev->addr = addr;
    dev->width = SSD1306_WIDTH;
    dev->height = SSD1306_HEIGHT;
    memset(dev->buffer, 0, SSD1306_BUF_SIZE);

    /* Init sequence */
    ssd1306_cmd(dev, SSD1306_DISPLAY_OFF);
    ssd1306_cmd(dev, SSD1306_SET_CLK_DIV);
    ssd1306_cmd(dev, 0x80);
    ssd1306_cmd(dev, SSD1306_SET_MUX_RATIO);
    ssd1306_cmd(dev, SSD1306_HEIGHT - 1);
    ssd1306_cmd(dev, SSD1306_SET_DISPLAY_OFFSET);
    ssd1306_cmd(dev, 0x00);
    ssd1306_cmd(dev, SSD1306_SET_START_LINE | 0x00);
    ssd1306_cmd(dev, SSD1306_SET_CHARGE_PUMP);
    ssd1306_cmd(dev, 0x14);  /* enable charge pump */
    ssd1306_cmd(dev, SSD1306_SET_MEM_ADDR_MODE);
    ssd1306_cmd(dev, 0x00);  /* horizontal addressing */
    ssd1306_cmd(dev, SSD1306_SET_SEG_REMAP | 0x01);
    ssd1306_cmd(dev, SSD1306_SET_COM_OUT_DIR | 0x08);
    ssd1306_cmd(dev, SSD1306_SET_COM_PIN_CFG);
    ssd1306_cmd(dev, 0x12);  /* 128x64 */
    ssd1306_cmd(dev, SSD1306_SET_CONTRAST);
    ssd1306_cmd(dev, 0xCF);
    ssd1306_cmd(dev, SSD1306_SET_PRECHARGE);
    ssd1306_cmd(dev, 0xF1);
    ssd1306_cmd(dev, SSD1306_SET_VCOM_DESEL);
    ssd1306_cmd(dev, 0x40);
    ssd1306_cmd(dev, SSD1306_ENTIRE_DISPLAY_ON);
    ssd1306_cmd(dev, SSD1306_SET_NORM_INV | 0x00);
    ssd1306_cmd(dev, SSD1306_DISPLAY_ON);

    ssd1306_clear(dev);
    printf("[SSD1306] Init OK at 0x%02X\n", addr);
    return true;
}

void ssd1306_show(ssd1306_t *dev) {
    ssd1306_cmd(dev, SSD1306_SET_COL_ADDR);
    ssd1306_cmd(dev, 0);
    ssd1306_cmd(dev, dev->width - 1);
    ssd1306_cmd(dev, SSD1306_SET_PAGE_ADDR);
    ssd1306_cmd(dev, 0);
    ssd1306_cmd(dev, SSD1306_PAGES - 1);

    /* Send buffer in chunks (I2C has transfer limits) */
    uint8_t chunk[129];  /* 1 control byte + 128 data bytes */
    chunk[0] = 0x40;     /* Co=0, D/C#=1 = data */

    for (int i = 0; i < SSD1306_BUF_SIZE; i += 128) {
        int len = SSD1306_BUF_SIZE - i;
        if (len > 128) len = 128;
        memcpy(&chunk[1], &dev->buffer[i], len);
        i2c_write_blocking(dev->i2c, dev->addr, chunk, len + 1, false);
    }
}

void ssd1306_fill(ssd1306_t *dev, uint8_t color) {
    memset(dev->buffer, color ? 0xFF : 0x00, SSD1306_BUF_SIZE);
}

void ssd1306_clear(ssd1306_t *dev) {
    ssd1306_fill(dev, 0);
    ssd1306_show(dev);
}

void ssd1306_pixel(ssd1306_t *dev, int16_t x, int16_t y, uint8_t color) {
    if (x < 0 || x >= dev->width || y < 0 || y >= dev->height) return;

    /* MONO_VLSB format: each byte is a vertical 8-pixel column */
    uint16_t idx = x + (y / 8) * dev->width;
    if (color) {
        dev->buffer[idx] |= (1 << (y & 7));
    } else {
        dev->buffer[idx] &= ~(1 << (y & 7));
    }
}

void ssd1306_text(ssd1306_t *dev, const char *str, int16_t x, int16_t y, uint8_t color) {
    while (*str) {
        uint8_t c = (uint8_t)*str;
        if (c >= 32 && c <= 126) {
            const uint8_t *glyph = font_5x7[c - 32];
            for (int col = 0; col < 5; col++) {
                uint8_t bits = glyph[col];
                for (int row = 0; row < 7; row++) {
                    if (bits & (1 << row)) {
                        ssd1306_pixel(dev, x + col, y + row, color);
                    } else if (!color) {
                        ssd1306_pixel(dev, x + col, y + row, 1);
                    }
                }
            }
        }
        x += 6;  /* 5 pixels + 1 gap */
        str++;
    }
}

void ssd1306_hline(ssd1306_t *dev, int16_t x, int16_t y, int16_t w, uint8_t color) {
    for (int16_t i = 0; i < w; i++) {
        ssd1306_pixel(dev, x + i, y, color);
    }
}

void ssd1306_vline(ssd1306_t *dev, int16_t x, int16_t y, int16_t h, uint8_t color) {
    for (int16_t i = 0; i < h; i++) {
        ssd1306_pixel(dev, x, y + i, color);
    }
}

void ssd1306_rect(ssd1306_t *dev, int16_t x, int16_t y, int16_t w, int16_t h, uint8_t color) {
    ssd1306_hline(dev, x, y, w, color);
    ssd1306_hline(dev, x, y + h - 1, w, color);
    ssd1306_vline(dev, x, y, h, color);
    ssd1306_vline(dev, x + w - 1, y, h, color);
}

void ssd1306_fill_rect(ssd1306_t *dev, int16_t x, int16_t y, int16_t w, int16_t h, uint8_t color) {
    for (int16_t j = 0; j < h; j++) {
        ssd1306_hline(dev, x, y + j, w, color);
    }
}

void ssd1306_contrast(ssd1306_t *dev, uint8_t value) {
    ssd1306_cmd(dev, SSD1306_SET_CONTRAST);
    ssd1306_cmd(dev, value);
}

void ssd1306_invert(ssd1306_t *dev, bool on) {
    ssd1306_cmd(dev, SSD1306_SET_NORM_INV | (on ? 1 : 0));
}
